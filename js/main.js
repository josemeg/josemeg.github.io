var app = new Vue({
  el: "#app",
  data: {
    message: "Hello Vue!",
    currentLanguage: "",
    projects: [],
    tech: [],
    customers: [],
    hidePopup: true,
    currentProject: [],
    languages: [
      { code: "ca", text: "_catalan" },
      { code: "es", text: "_spanish" },
      { code: "en", text: "_english" },
    ],
  },
  methods: {
    onChange(ev) {
      this.currentLanguage = ev.target.value
    },
    $t(literal) {
      switch (this.currentLanguage) {
        case "en":
          return _TEXT.en[literal] || literal
        case "es":
          return _TEXT.es[literal] || literal
        case "ca":
          return _TEXT.ca[literal] || literal
        default:
          return _TEXT.en[literal] || literal
      }
    },
    background() {
      this.hidePopup = !this.hidePopup
      this.currentProject = []
    },
    showProject(project) {
      this.currentProject = project
      this.hidePopup = !this.hidePopup
    },
  },
  beforeMount() {
    let userLang = navigator.language || navigator.userLanguage

    if (userLang === "es-ES" || userLang === "es") this.currentLanguage = "es"
    else if (userLang === "ca-ES" || userLang === "ca") this.currentLanguage = "ca"
    else this.currentLanguage = "en"

    this.projects = _PROJECTS
    this.tech = _TECH
    this.customers = _CUSTOMERS
  },
})
